package constructor;

public class FinalKeyword {

	//// we can also mark the whole class as final;
	// that means you can not extend that class;

	// if we declare the method as final
	// we can not overRide the method;

	// finally keyword is related to try catch exaption
	// once we executed try or catch block
	// if we face any error, control will still come to this block and executed
	// the code no matter the scipt is pass or fail.

	public static void main(String[] args) {
		/*
		 * int i = 4; i = 5; System.out.println(i);
		 */

		final int i = 4; // constance variable;

	}

}
